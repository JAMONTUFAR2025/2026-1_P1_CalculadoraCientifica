package dominio;

/* Clase para poder calcular logaritmos con un numero */
public class Logaritmos
{
    private double numero;

    public Logaritmos(double numero)
    {
        this.numero = numero;
    }

    public double getNumero()
    {
        return numero;
    }

    public void setNumero(double numero)
    {
        this.numero = numero;
    }
}
